// ── Haversine great-circle distance (miles) ────────────────
function airportDistance(code1, code2) {
  const a = AIRPORT_COORDS[code1];
  const b = AIRPORT_COORDS[code2];
  if (!a || !b) return null;
  const R = 3959; // Earth radius miles
  const lat1 = a[1] * Math.PI / 180, lat2 = b[1] * Math.PI / 180;
  const dLat = (b[1] - a[1]) * Math.PI / 180;
  const dLon = (b[0] - a[0]) * Math.PI / 180;
  const h = Math.sin(dLat/2)**2 + Math.cos(lat1)*Math.cos(lat2)*Math.sin(dLon/2)**2;
  return R * 2 * Math.atan2(Math.sqrt(h), Math.sqrt(1-h));
}

// Returns { miles, flightHours, rangeLabel, stopsNote } or null
function estimateTravelTime(homeCode, destCode) {
  if (!homeCode || !destCode) return null;
  const miles = airportDistance(homeCode.toUpperCase(), destCode.toUpperCase());
  if (miles === null) return null;
  const rawHours = miles / 550 + 1; // avg cruise 550 mph + 1 hr overhead
  const low  = Math.round(rawHours * 10) / 10;
  const high = Math.round(rawHours * 1.3 * 10) / 10; // 30% buffer for connections
  let stopsNote;
  if (rawHours < 4)       stopsNote = 'likely a direct flight';
  else if (rawHours < 9)  stopsNote = 'direct or one connection';
  else if (rawHours < 15) stopsNote = 'one to two connections typical';
  else                    stopsNote = 'two or more connections expected';
  return {
    miles: Math.round(miles),
    rangeLabel: `${low}–${high} hrs`,
    stopsNote
  };
}
